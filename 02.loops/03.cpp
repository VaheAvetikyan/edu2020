/*
    3: Միջակայքի 7֊ի բազմապատիկներ
    Պահանջվում է գրել ծրագիր, որը մուտքում կստանա A և B ամբողջ թվերը և կարտածի [A, B] միջակայքի բոլոր այն թվերի քանակը, որոնք առանց մնացորդի բաժանվում են 7֊ի։
*/

#include <iostream>

int main()
{
    int A, B;
    std::cin >> A >> B;
    int count = 0;

    while (A <= B)
    {
        if (A % 7 == 0)
        {
            count++;
        }
        A++;
    }
    std::cout << count << std::endl;
}