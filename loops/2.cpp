/*
    2: Միջակայքի զույգ թվեր
    Գրել ծրագիր, որը մուտքում կստանա A և B ամբողջ թվերը և կարտածի [A, B] միջակայքի բոլոր զույգ թվերը։ 
    Արտածվող թվերը միմյանցից պետք է բաժանված լինեն բացատանիշով։
*/

#include <iostream>

int main()
{
    int A, B;
    std::cin >> A >> B;

    while (A <= B)
    {
        if (A % 2 == 0)
        {
            std::cout << A << " ";
        }
        A++;
    }
    std::cout << std::endl;
}